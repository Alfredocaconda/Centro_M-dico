    private void btregistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btregistarActionPerformed
        String Codigo, Nome, Email, Senha, Utilizador, Especialidade;
        Codigo = txtcodigo1.getText();
        Nome = txtnome.getText();
        Email = txtemail.getText();
        Senha = pwsenha.getText();
        Utilizador = (String) cbutilizador.getSelectedItem();
        Especialidade = (String) cbespecialidade.getSelectedItem();
        String caminho = "C:/Lista/Registo.txt";
        File f = new File(caminho);
        try {
            FileWriter Registo = new FileWriter(f,true);
            Registo.write(Codigo+";"+Nome+";"+Email+";"+Senha+";"+Utilizador+";"+Especialidade+"/n");
            Registo.close();
            JOptionPane.showMessageDialog(this, "Registado com sucesso");
            txtcodigo1.setText("");
            txtemail.setText("");
            txtnome.setText("");
            pwsenha.setText("");
            cbutilizador.setSelectedIndex(0);
            txtcodigo1.requestFocus();
                   
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "N√£o Registado");
        }
        
    }//GEN-LAST:event_btregistarActionPerformed

    private void btlistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btlistarActionPerformed
        String caminho = "C:/Lista/Registo.txt";
        File f = new File(caminho);
        
        try {
            Scanner s = new Scanner(f);
            while (s.hasNext()) {
                DefaultTableModel dtm = (DefaultTableModel) tbdados.getModel();
        dtm.setNumRows(0);
                Object[] next = s.next().split(";");
                dtm.addRow(next);
                
                
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btlistarActionPerformed

