    private void buscarespecialidade() {
        String c ="C:/Lista/Registo.txt";
        File f = new File(c);
       
        try {
            
             Scanner s = new Scanner (f);
            while (s.hasNextLine()) {
                
                String[] x= s.nextLine().split("sep");
                cbespecialidade.addItem(x[1]+" -- "+x[2]);
            }
                
        } catch (Exception e) {
                System.out.println(e);
        }
           
                
           }
        
    
